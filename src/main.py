""" Main. """

import window
import sys
from PyQt4 import QtGui

if __name__ == '__main__':
    APP = QtGui.QApplication(sys.argv)
    documents = sys.argv[1:]
    if documents:
        for doc in documents:
            WINDOW = window.MainWindow(document=doc)
            WINDOW.show()
    else:
        WINDOW = window.MainWindow()
        WINDOW.show()

    sys.exit(APP.exec_())
